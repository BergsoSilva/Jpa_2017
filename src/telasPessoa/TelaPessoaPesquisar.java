    private void botaoDuplicarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoDuplicarActionPerformed
        // TODO add your handling code here:
         PessoaControle controle = new PessoaControle();
         List<Pessoa> pessoasAux = new ArrayList<>();
         
        try {
            EntityManager em = JPAUtil.getJPAUtil();
            Pessoa pessoaDouble = pessoas.get(tabelaPessoa.getSelectedRow());
            Pessoa pessoaAux = em.find(Pessoa.class, pessoaDouble.getId());
            pessoasAux = controle.listar();
            em.getTransaction().begin();
            em.detach(pessoaAux);
            Pessoa p1 = pessoaAux;
            p1.setId(pessoasAux.get(pessoasAux.size()-1).getId()+1);
            controle.alterar(p1);
            em.getTransaction().commit();
            em.close();
            JOptionPane.showMessageDialog(null, "pessoa duplicada");
            // controle.inserir(pessoaDouble);
            preencharTabela();
        } catch (ConstraintViolationException cex) {
            for (ConstraintViolation c : cex.getConstraintViolations()) {
                JOptionPane.showMessageDialog(null, c.getMessage());
            }
        }
    }//GEN-LAST:event_botaoDuplicarActionPerformed

